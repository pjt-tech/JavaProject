package com.cyberkyj.predicate;

public class Student {
	
	String name;
	String s;
	int score;
	
	public Student(String name, String s, int score) {
		this.name = name;
		this.s = s;
		this.score = score;
	}

	public String getS() {
		return s;
	}

	public int getScore() {
		return score;
	}
	
	
	
	

}
